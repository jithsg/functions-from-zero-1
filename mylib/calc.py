"""
A calculator module
"""


def add(a, b):
    """
    Add two numbers
    """
    return a + b


def sub(a, b):
    """
    Subtract two numbers
    """
    return a - b


def mul(a, b):
    """
    Multiply two numbers
    """
    return a * b


def div(a, b):
    """
    Divide two numbers
    """
    return a / b


def sqrt(a):
    """
    Square root of a number
    """
    return a**0.5
